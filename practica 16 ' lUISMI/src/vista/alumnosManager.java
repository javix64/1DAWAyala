    public void cambiarNotasVista() {

        int contador = tabla.getRowCount();
        for (int i = 0; i < contador; i++) {

        

                controlador.cambiarNotas(tabla.getValueAt(i, 0).toString(), (String) comoEva.getSelectedItem(),
                        (String) comboCurso.getSelectedItem(),
                        (String) comboAsig.getSelectedItem(), tabla.getValueAt(i, 1).toString());

         

        }
    }

    public void rellenarNotas() {

        int contador = cuadro2.getItemCount();
        for (int i = 0; i < contador; i++) {

            try {

                String nota = controlador.traerNota(cuadro2.getItem(i), (String) comboCurso.getSelectedItem(),
                        (String) comboAsig.getSelectedItem(), (String) comoEva.getSelectedItem());
                //label.setText(""+ ArrayEva.size());

           
                tabla.setValueAt(nota, i, 1);

            } catch (Exception e) {

                System.out.println("Fallo en rellenarNotas");
            }

        }
    }


