    private void rellenarCurso() {
        comboCurso.removeAllItems();
        ResultSet resultado = controlador.obtenerCursos();

        try {

            if (resultado != null) {

                while (resultado.next()) {

                    comboCurso.addItem(resultado.getString("curso"));

                }

            }

        } catch (Exception e) {

            System.out.println("Fallo rellenarCurso()");

        }

    }

    private void rellenarAsignaturas() {

        ResultSet resultado = controlador.obtenerAsignaturas((String) comboCurso.getSelectedItem());
        comboAsig.removeAllItems();;
        try {

            if (resultado != null) {

                while (resultado.next()) {

                    comboAsig.addItem(resultado.getString("asignatura"));

                }

            }

        } catch (Exception e) {

            System.out.println("Fallo rellenarCurso()");

        }

    }

    public void traerAlumnos() {

        ArrayList<Alumnado> ArrayAlumnos = controlador.guardarAlumnos((String) comboCurso.getSelectedItem(), (String) comboAsig.getSelectedItem());
        if (ArrayAlumnos != null) {
            for (int i = 0; i < ArrayAlumnos.size(); i++) {

                cuadro1.add(ArrayAlumnos.get(i).getAlumno());

            }
        }
    }

